from turtle import *

# Създаваме екран за рисуване
screen = Screen()
screen.title("Демо за промяна на размерите на героя")

# Създаваме нашия герой (костенурка) и задаваме цветове за очертанията и запълването
turtle = Turtle()
turtle.shape("turtle")
turtle.color("blue", "lightgreen")  # Очертания в синьо, запълване в светлозелено
turtle.penup()
turtle.goto(-250, 0)
turtle.speed(1)

# Пример 1: Увеличаваме 2 пъти широчината и дължината на героя
turtle.shapesize(2)  # Увеличаваме 2 пъти стандартната широчина и дължина
turtle.stamp()  # Оставяме отпечатък

# Пример 2: Увеличаваме 2 пъти широчината и 4 пъти дължината на героя
turtle.goto(-100, 0)
turtle.shapesize(2, 4)  # 2 пъти широчина, 4 пъти дължина
turtle.stamp()

# Пример 3: Увеличаваме 2 пъти широчината, 4 пъти дължината и дебелина 5 на очертанията
turtle.goto(50, 0)
turtle.shapesize(2, 4, 5)  # 2 пъти широчина, 4 пъти дължина, дебелина 5
turtle.stamp()

# Пример 4: Възстановяване на началния размер на формата (1, 1, 1)
turtle.goto(200, 0)
turtle.shapesize(1, 1, 1)  # Връщаме героя до стандартните размери
turtle.stamp()

# Скриваме героя след като отпечатъците са направени
turtle.hideturtle()

# Изчакваме потребителя да затвори прозореца
screen.mainloop()
