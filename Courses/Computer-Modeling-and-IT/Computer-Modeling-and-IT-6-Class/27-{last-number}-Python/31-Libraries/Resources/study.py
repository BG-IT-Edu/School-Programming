import random

# Генерираме случайно число между 1 и 100
random_number = random.randint(1, 100)

# Даваме на потребителя 3 опита да познае числото
for attempt in range(3):
    # Потребителят въвежда предположение
    guess = int(input("Познайте числото между 1 и 100: "))

    # Проверяваме дали предположението е правилно
    if guess == random_number:
        print("Поздравления! Познахте числото!")
        break  # Излизаме от цикъла, защото числото е познато
    elif guess < random_number:
        print("Числото е по-голямо от вашето предположение.")
    else:
        print("Числото е по-малко от вашето предположение.")

# Ако след трите опита числото не е познато, го показваме
else:
    print(f"Времето изтече! Правилното число беше {random_number}.")
